package Doit_dowhile;

public class While_do_while {

	public static void main(String[] args) {
		//while
		int a;
		a=0;
		while(a<10) {
			
			System.out.print(a+" ");
			a++;
		}
		System.out.println();
		
		//do-while
		
		a=0;
		do {
			System.out.print(a+" ");
			a++;
		}while(a<10);
	}

}
